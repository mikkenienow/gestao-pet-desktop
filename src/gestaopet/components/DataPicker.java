    public String getData(){
        if(dataDefinida){
           return DateTools.dateToString(data);
        } else {
           return "";
        }
    }

    public void setInput(String data){
        
        String[] dt = data.split("/");
        int d = Integer.parseInt(dt[0]);
        int m = Integer.parseInt(dt[1]);
        String y = dt[2];
        
        for(int i = 0; i < year.getItemCount(); i++){
            String y2 = year.getItemAt(i);
            if(y2.equals(y)){
                year.setSelectedIndex(i);
            }
        }
        month.setSelectedIndex(m-1);
        day.setSelectedIndex(d-1);
    }


    
    public int year(){
        return Integer.parseInt((String)year.getSelectedItem());
    }
    
    public int month(){
        return month.getSelectedIndex() + 1;
    }

    public int day(){
        return Integer.parseInt((String)day.getSelectedItem());
    }
    
    private void loadYear(){
        int y = DateTools.getYear()+10;
        
        for(int i = 0; i < y - 1995; i++){
            year.addItem((1995 + i) + "");
        }
    }
    
    private void loadDays(){
        day.setSize(50, HEIGHT);
        day.removeAllItems();
        int[] days = DateTools.getDaysInMonth(year(), month());
        for(int i = 0; i < days.length; i++){
            day.addItem(days[i] + "");
        }
    }
    
    public void confirm(){
        data = DateTools.getDate(DateMethods.NEWDAY, year(), month(), day());
        dataDefinida = true;
        
        try {
            j.setText(DateTools.dateToString(data));
            j.firePropertyChange(accessibleContext.ACCESSIBLE_TEXT_PROPERTY, false, true);
        } catch (Exception e) {
            f.setText(DateTools.dateToString(data));
            f.onClick(confirm);
        }
        close();
    }
    
    public void cancel(){
        dataDefinida = false;
        close();
    }
    
